import fs from 'node:fs/promises'
import path from 'node:path'

const root = process.cwd()
const mapPath = path.join(root, 'i18n-map.json')
const map = JSON.parse(await fs.readFile(mapPath, 'utf8'))

const sources = {
  zh: path.join(root, 'tutorial_zh.md'),
  en: path.join(root, 'en', 'tutorial.md')
}

const splitRules = {
  zh: /^##\s+第\s*(\d+)\s*章[^\n]*$/gm,
  en: /^##\s+Chapter\s+(\d+)\s*:[^\n]*$/gm
}

function normalizeId(value) {
  return String(Number(value)).padStart(2, '0')
}

function splitByChapter(content, locale) {
  const rule = splitRules[locale]
  const matches = [...content.matchAll(rule)]
  const result = new Map()

  for (let i = 0; i < matches.length; i += 1) {
    const match = matches[i]
    const id = normalizeId(match[1])
    const start = match.index
    const end = i + 1 < matches.length ? matches[i + 1].index : content.length
    result.set(id, content.slice(start, end).trim() + '\n')
  }

  return result
}

function promoteChapterHeading(markdown, title) {
  return markdown.replace(/^##\s+[^\n]+/m, `# ${title}`)
}

function indexPage(locale) {
  const isZh = locale === 'zh'
  const title = isZh ? 'OSRCORE 教程章节索引' : 'OSRCORE Tutorial Chapter Index'
  const intro = isZh
    ? '本页由 `scripts/split-docs.mjs` 根据双语教程源文件自动生成，用于按章节阅读和维护中英文版本对齐。'
    : 'This page is generated by `scripts/split-docs.mjs` from the bilingual tutorial sources. It is used for chapter-based reading and bilingual alignment maintenance.'

  const lines = [`# ${title}`, '', intro, '']
  for (const chapter of map.chapters) {
    const text = locale === 'zh' ? chapter.zhTitle : chapter.enTitle
    const file = locale === 'zh' ? chapter.zh : chapter.en
    const href = locale === 'en'
      ? file.replace(/^en\//, '').replace(/\.md$/, '')
      : file.replace(/\.md$/, '')
    lines.push(`- [${text}](./${href})`)
  }
  lines.push('')
  return lines.join('\n')
}

async function writeFileEnsured(relativePath, content) {
  const absolutePath = path.join(root, relativePath)
  await fs.mkdir(path.dirname(absolutePath), { recursive: true })
  await fs.writeFile(absolutePath, content, 'utf8')
}

const sourceText = {
  zh: await fs.readFile(sources.zh, 'utf8'),
  en: await fs.readFile(sources.en, 'utf8')
}

const chaptersByLocale = {
  zh: splitByChapter(sourceText.zh, 'zh'),
  en: splitByChapter(sourceText.en, 'en')
}

const missing = []

for (const chapter of map.chapters) {
  const zh = chaptersByLocale.zh.get(chapter.id)
  const en = chaptersByLocale.en.get(chapter.id)

  if (!zh) missing.push(`zh chapter ${chapter.id}`)
  if (!en) missing.push(`en chapter ${chapter.id}`)

  if (zh) {
    await writeFileEnsured(chapter.zh, promoteChapterHeading(zh, chapter.zhTitle))
  }
  if (en) {
    await writeFileEnsured(chapter.en, promoteChapterHeading(en, chapter.enTitle))
  }
}

await writeFileEnsured('chapters.md', indexPage('zh'))
await writeFileEnsured('en/chapters.md', indexPage('en'))

if (missing.length > 0) {
  console.warn(`[split-docs] Missing chapters: ${missing.join(', ')}`)
  process.exitCode = 1
} else {
  console.log(`[split-docs] Generated ${map.chapters.length * 2} chapter pages and chapter indexes`)
}
